import Foundation

class MathematicFunctions {
    
    func add(x: Int, y: Int) -> Int {
        return x + y
    }
    
    func subtract(x: Int, y: Int) -> Int {
        return x - y
    }
    
    func divide(x: Int, y: Int) -> Int {
        return x / y
    }
}
